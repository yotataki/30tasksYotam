"""
if what the tasks means is to display the sum of 5 numbers, here's the code:
"""

total = 0  # Step 1

for _ in range(5):  # Step 2
    input_number = float(input("Enter a number: "))  # Step 3
    total += input_number  # Step 4

# Step 5
print(f"The sum of the 5 pieces of information is: {total}")
